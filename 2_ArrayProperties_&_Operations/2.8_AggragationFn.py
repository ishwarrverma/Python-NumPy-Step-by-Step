"""
 function           what it does

np.sum(array)      add all element
np.mean(array)     calculate average
np.min(array)      smallest value
np.max(array)      max value
np.std(array)      calculate standard deviation
np.var(arr)        calculate variance   

"""
import numpy as np
arr = np.array([10,20,30,40,50])

print(np.sum(arr))
print(np.mean(arr))
print(np.min(arr))
print(np.max(arr))
print(np.std(arr))
print(np.var(arr))
